    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed

        try{
            
            restColorText(txCodigo);
        Integer codSel = Integer.parseInt(txCodigo.getText());
        
        productoActual = proData.buscarProductoCd(codSel);
        
            if (productoActual!= null) {
                
        txNombre.setText(productoActual.getNombre());
        txPrecio.setText(String.valueOf(productoActual.getPrecio()));
        txCantidad.setText(String.valueOf(productoActual.getCantidad()));
        cbxEstado.setSelected(productoActual.isEstado());
                
            }

        } catch (NumberFormatException ex) {
            
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR UN NUMERO VALIDO" + ex.getMessage());
            setColorText(txCodigo);
            
        }
        


    }//GEN-LAST:event_btBuscarActionPerformed

    private void btGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGuardarActionPerformed

        try{
        
            restColorText(txNombre);
            restColorText(txCodigo);
            restColorText(txPrecio);
            restColorText(txCantidad);
            
            
        Integer codSel = Integer.parseInt(txCodigo.getText());
        
        String nombreSel = txNombre.getText();
        double precioSel = Double.parseDouble(txPrecio.getText());
        Integer cantiSel = Integer.parseInt(txCantidad.getText());
        boolean estadoSel = cbxEstado.isSelected();
        
            if (nombreSel.isEmpty()) {
                
                JOptionPane.showMessageDialog(null, "Debe ingresar un nombre");
                setColorText(txNombre);
                return;
                
            } if (precioSel == 0) {
                 JOptionPane.showMessageDialog(null, "Debe ingresar un Precio");
                setColorText(txPrecio);
                return;
            }if (cantiSel == 0) {
                 JOptionPane.showMessageDialog(null, "Debe ingresar una cantidad");
                setColorText(txCantidad);
                return;
            }
            
            
        
            Producto producto = null;
        
            if (productoActual == null) {
                
                producto = new Producto(codSel, nombreSel, cantiSel, precioSel, estadoSel);
                proData.cargarProducto(producto);
                
            } else {
                
                producto  = productoActual;
                
                producto.setCodigo(codSel);
                producto.setNombre(nombreSel);
                producto.setCantidad(cantiSel);
                producto.setPrecio(precioSel);
                producto.setEstado(estadoSel);
                
                proData.modificarProducto(producto);
                
            }
        
        } catch (NumberFormatException ex){
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR UN CODIGO" + ex.getMessage());
            setColorText(txCodigo);
        }
  

    }//GEN-LAST:event_btGuardarActionPerformed

